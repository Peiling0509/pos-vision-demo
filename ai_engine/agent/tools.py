@tool
def get_inventory(product_name: str) -> dict:
    """
    Query the product inventory, price, and active status from the database.
    Use this tool when the user asks about the stock, inventory, quantity, or price of a specific product.
    
    Args:
        product_name: The exact name of the product to search for (e.g., "Dutch Lady").
    """
    try:
        connection = get_db_connection()
        with connection.cursor() as cursor:
            sql = """
                SELECT name, stock_quantity, price, is_active 
                FROM products 
                WHERE name LIKE %s 
                LIMIT 1
            """

            cursor.execute(sql, (f"%{product_name}%",))
            result = cursor.fetchone()
            
        connection.close()
        
        if result:
            return {
                "product": result["name"],
                "stock": result["stock_quantity"],
                "price": float(result["price"]), 
                "status": "In Stock" if result["stock_quantity"] > 0 else "Out of Stock",
                "is_active": "Active" if result["is_active"] else "Discontinued"
            }
        else:
            return {"error": f"I couldn't find any product matching '{product_name}' in our database."}
            
    except Exception as e:
        return {"error": f"Database query failed: {str(e)}"}
    

